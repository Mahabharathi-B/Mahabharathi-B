class NoSeries2452 {
    public static void main (String[] args){
      int x , y, z,k;
       
	for (x=1;x<=5;x++){
             for(y=1;y<=x;y++){
	
	      System.out.print("*");
               for (k=4;k>0;k--){
	         for (z=1;z<=k;z++){
	               System.out.print("#");
                    }
	     }
       }
  }    

    System.out.println("");
         

 }


}